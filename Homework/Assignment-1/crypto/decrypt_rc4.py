from Crypto.Cipher import ARC4

# Obtained from GDB: `x/100bx &bonus_flag`
enc_flag = b'\x21\x07\x89\x1f\x0d\x3b\x96\x26\xd1\x92\xb3\x5f\x41\x8a\x5b\xb5\xb1\xde' + \
	b'\x1a\xd1\x45\x0f\xce\xb6\xef\xc1\x45\x02\xa6\x20\xe0\x7a\xf1\x06\xe8' +\
	b'\x88\xc7\xc1\x1e\xed\xca\x95\x23\x86\x46\x26\xc8\x0b\x1f\xee\xc1\x16\xf5\x40\x22\xb1' +\
	b'\xb8\x65\x4c\x65\xb9\x4c\x3e\xb7\xeb\xf1\x50\x7e\xaf\x40\x6d\xa6' +\
	b'\x9a\xd2\xee\x3a\x93\x62\x32\x54\xf2\x68\x69\x8e\xcc\x6e\x99\xcd' +\
	b'\x37\xde\xd1\x45\x54\x04\x74\xf8\x43\xbf\x03\x95'

cipher = ARC4.new('rc4')
flag = cipher.decrypt(enc_flag)

# It should be CNS_CTF{...}, not CNSCTF_{...}
print(flag.decode())
